# 6. Escribe un programa que pida el año actual y
# el de nacimiento del usuario.
# Debe calcular su edad, suponiendo que en el año en
# curso el usuario ya ha cumplido años.

año = int(input('Introduza el año actual: '))
nace = int(input('Introduzca su año de nacimiento: '))
print( 'Usted tiene', año - nace , 'años.')
